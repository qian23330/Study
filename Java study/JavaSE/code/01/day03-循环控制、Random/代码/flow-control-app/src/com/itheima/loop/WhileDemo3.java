package com.itheima.loop;

public class WhileDemo3 {
    public static void main(String[] args) {
        // 目标：掌握while循环的书写格式，以及理解其执行流程。
        // 需求：打印多行Hello World
        int i = 0;
        while (i < 5) {
            // i = 0 1 2 3 4
            System.out.println("Hello World");
            i++;
        }
    }
}
